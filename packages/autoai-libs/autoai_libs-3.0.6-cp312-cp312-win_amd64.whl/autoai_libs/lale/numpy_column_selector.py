import autoai_libs.transformers.exportable
import pandas as pd

import lale.docstrings
import lale.operators

from ._common_schemas import _hparams_column_index_list


class _NumpyColumnSelectorImpl:
    def __init__(self, **hyperparams):
        self._wrapped_model = autoai_libs.transformers.exportable.NumpyColumnSelector(**hyperparams)

    def fit(self, X, y=None):
        if isinstance(X, pd.DataFrame):
            X = X.to_numpy()
        self._wrapped_model.fit(X, y)
        return self

    def transform(self, X):
        if isinstance(X, pd.DataFrame):
            X = X.to_numpy()
        return self._wrapped_model.transform(X)


